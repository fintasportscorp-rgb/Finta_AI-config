/* English defaults captured from the DOM so we can switch back */
const LOOPFORGE_EN_DEFAULTS = {};

function loopforgeApplyLang(lang) {
  const dict = lang === 'en' ? LOOPFORGE_EN_DEFAULTS : LOOPFORGE_I18N[lang];
  if (!dict) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  document.documentElement.lang = lang;
  try { localStorage.setItem('finta-lang', lang); } catch (e) {}
  const sel = document.getElementById('lang-select');
  if (sel) sel.value = lang;
}

function loopforgeInitLang() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    if (!(el.dataset.i18n in LOOPFORGE_EN_DEFAULTS)) LOOPFORGE_EN_DEFAULTS[el.dataset.i18n] = el.innerHTML;
  });
  let lang = new URLSearchParams(location.search).get('lang');
  if (!lang) { try { lang = localStorage.getItem('finta-lang'); } catch (e) {} }
  if (!lang) lang = (navigator.language || 'en').slice(0, 2);
  if (!LOOPFORGE_I18N[lang] && lang !== 'en') lang = 'en';
  loopforgeApplyLang(lang);
  const sel = document.getElementById('lang-select');
  if (sel) sel.addEventListener('change', e => loopforgeApplyLang(e.target.value));
}

document.addEventListener('DOMContentLoaded', loopforgeInitLang);
